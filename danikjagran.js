function loadJagranStates() {
    const states = [...new Set(jagranData.map(entry => entry.state))];
    const stateSelect = document.getElementById("jagranState");
    states.forEach(state => {
        const option = document.createElement("option");
        option.value = state;
        option.textContent = state;
        stateSelect.appendChild(option);
    });
}

function updateJagranCities() {
    const selectedState = document.getElementById("jagranState").value;
    const citySelect = document.getElementById("jagranCity");
    citySelect.innerHTML = '';
    jagranData
        .filter(entry => entry.state === selectedState)
        .forEach(entry => {
            const option = document.createElement("option");
            option.value = entry.slug;
            option.textContent = entry.city;
            citySelect.appendChild(option);
        });
}

function setDefaultJagranSelection() {
    const stateSelect = document.getElementById("jagranState");
    const citySelect = document.getElementById("jagranCity");

    stateSelect.value = "Delhi";
    updateJagranCities();

    const nationalCity = jagranData.find(entry => entry.city === "National (राष्ट्रीय संस्करण)" && entry.state === "Delhi");
    if (nationalCity) {
        citySelect.value = nationalCity.slug;
    }
}

async function downloadDanikJagran() {

    const date = document.getElementById('jagranDate').value;
    const citySlug = document.getElementById('jagranCity').value;
    const downloadingMessage = document.createElement('p');
    downloadingMessage.classList.add('downloading-message');
    downloadingMessage.textContent = 'Downloading, please wait...';

    const downloadButton = document.querySelector('#jagranForm button');
    const mainContainer = document.getElementById('mainContainer');
    const previewElement = document.getElementById('preview');

    // Disable button and show downloading message
    downloadButton.disabled = true;
    mainContainer.appendChild(downloadingMessage);
    downloadingMessage.style.display = 'block';

    let [year, month, day] = date.split("-");

    const months = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];

    const monthName = months[parseInt(month) - 1];

    try {
        const response = await fetch(`https://d3f65smzvvdjuh.cloudfront.net/dev/v1/download?citySlug=${citySlug}&day=${day}&month=${monthName}&year=${year}`, {
            method: 'GET',
            headers: { 'Content-Type': 'application/json' }
        });

        const data = await response.json();

        if (data && data.data.htmlContent) {
            mainContainer.style.display = 'none';
            document.getElementById('footer').style.display = 'none';
            previewElement.innerHTML = data.data.htmlContent;
        } else {
            throw new Error("No data returned from the API.");
        }

    } catch (error) {
        console.error('Error:', error);
    } finally {
        downloadButton.disabled = false;
        downloadingMessage.style.display = 'none';
    }
}

const defaultDanikJagranOption = () => {

    const currentDate = new Date();
    const formattedDate = `${currentDate.getFullYear()}-${(`0${currentDate.getMonth() + 1}`).slice(-2)}-${(`0${currentDate.getDate()}`).slice(-2)}`;

    document.getElementById('jagranDate').value = formattedDate;
    loadJagranStates();
    setDefaultJagranSelection();
    document.getElementById('jagranState').addEventListener('change', updateJagranCities);
};
